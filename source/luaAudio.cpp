#include <stdlib.h>
#include <string.h>
#include <unistd.h>
#include <3ds.h>
#include "include/luaAudio.h"

/* Custom CSND_playsound: 
 - Prevent audio desynchronization for Video module
 - Enable stereo sounds
 - Add ADPCM codec support
*/
void My_CSND_playsound(u32 channel, u32 looping, u32 encoding, u32 samplerate, u32 *vaddr0, u32 *vaddr1, u32 totalbytesize, u32 l_vol, u32 r_vol)
{
	// this ignores the l_vol and r_vol settings but since CHMM2 seems to always set those to max it shouldn't really matter...
	csndPlaySound(channel, SOUND_LOOPMODE(looping) | SOUND_FORMAT(encoding), samplerate, 1.0f, 0.0f, vaddr0, vaddr1, totalbytesize);
}
